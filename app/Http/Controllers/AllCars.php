    {
        $allcars = Car::all();
        $types = CarType::all();
        $request->tipe_mobil;
        if(!empty($request->tipe_mobil)){
            if(count($request->tipe_mobil) != count($types)){
                $allcars = $allcars->whereIn('tipe_mobil',$request->tipe_mobil);
            }
        }

        return view('car.all_car',compact('allcars','types'));
    }


